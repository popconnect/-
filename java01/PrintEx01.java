public class PrintEx01 {
    public static void main(String[] args){

        //println 줄내림
        System.out.println("HelloPrint");
        System.out.println("HelloPrint");
        System.out.println("HelloPrint");

        System.out.print("HelloPrint");
        System.out.print("HelloPrint");
        System.out.print("HelloPrint");

        //%s 문자열 %d 숫자, \n
        System.out.printf("%s %s \n", "hello", "world");
        System.out.printf("%d %d \n", 10, 20);
    }
    
}
